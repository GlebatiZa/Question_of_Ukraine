score = 0  # лічильник правильних відповідей

# Питання 1
a = int(input("В якому році народився Тарас Шевченко? "))
if a == 1814:
    print("Правильно!")
    score += 1
else:
    print("Неправильно!")

# Питання 2
b = input("Яка столиця України? ").strip().lower()
if b in ["київ", "киев"]:
    print("Правильно!")
    score += 1
else:
    print("Неправильно!")

# Питання 3
c = input("Як називається Основний закон країни? ").strip().lower()
if c == "конституція":
    print("Правильно!")
    score += 1
else:
    print("Неправильно!")

# Питання 4
d = int(input("В якому році Україна стала незалежною? "))
if d == 1991:
    print("Правильно!")
    score += 1
else:
    print("Неправильно!")

# Питання 5
e = input("Яка мова є державною в Україні? ").strip().lower()
if e == "українська":
    print("Правильно!")
    score += 1
else:
    print("Неправильно!")

# Виведення результату
print(f"\nУ тебе {score} правильних відповідей з 5.")

# Оцінювання результату
if score == 5:
    print("Відмінно! Ти справжній знавець!")
elif score >= 3:
    print("Добре! Але є куди рости :)")
elif score >= 1:
    print("Слабенько. Варто повторити матеріал.")
else:
    print("Жодної правильної відповіді. Спробуй ще раз!")
